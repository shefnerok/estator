
public class Area {
	private String area;
	
	

	public Area(String area) {
		super();
		this.area = area;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	@Override
	public String toString() {
		return "Площа " + area + " ";
	}
	
	
	

}
